function createTray(): void {
  if (tray) {
    return;
  }

  try {
    tray = new Tray(buildTrayIcon());
  } catch (error) {
    const detail = error instanceof Error ? error.message : String(error);
    console.error(`[tray] failed to create system tray: ${detail}`);
    tray = null;
    return;
  }

  tray.setToolTip("OpenClaw Desktop - Gateway Unknown");
  tray.setContextMenu(buildTrayMenu());

  tray.on("click", () => {
    if (mainWindow && mainWindow.isVisible()) {
      mainWindow.hide();
      return;
    }

    showMainWindow();
  });

  void refreshTrayGatewayStatus(false).catch((error) => {
    const detail = error instanceof Error ? error.message : String(error);
    console.warn(`[tray] initial gateway refresh failed: ${detail}`);
  });
}

function broadcastSetupProgress(event: SetupProgressEvent): void {
  const setupLogLine = `[setup:${event.stage}:${event.source}] ${event.message}`;
  if (event.level === "error") {
    console.error(setupLogLine);
  } else if (event.level === "warning") {
    console.warn(setupLogLine);
  } else {
    console.info(setupLogLine);
  }

  if (!mainWindow || mainWindow.isDestroyed()) {
    pendingSetupEvents.push(event);
    if (pendingSetupEvents.length > 200) {
      pendingSetupEvents.shift();
    }
    return;
  }

  mainWindow.webContents.send("setup:progress", event);
}

function broadcastUpdateStatus(event: UpdateStatusEvent): void {
  if (!mainWindow || mainWindow.isDestroyed()) {
    pendingUpdateEvents.push(event);
    if (pendingUpdateEvents.length > 200) {
      pendingUpdateEvents.shift();
    }
    return;
  }

  mainWindow.webContents.send("update:status", event);
}

function flushPendingSetupEvents(): void {
  if (
    !mainWindow ||
    mainWindow.isDestroyed() ||
    pendingSetupEvents.length === 0
  ) {
    return;
  }

  for (const event of pendingSetupEvents) {
    mainWindow.webContents.send("setup:progress", event);
  }

  pendingSetupEvents.length = 0;
}

function flushPendingUpdateEvents(): void {
  if (
    !mainWindow ||
    mainWindow.isDestroyed() ||
    pendingUpdateEvents.length === 0
  ) {
    return;
  }

  for (const event of pendingUpdateEvents) {
    mainWindow.webContents.send("update:status", event);
  }

  pendingUpdateEvents.length = 0;
}

function resolveInstallNodeStage(message: string): SetupProgressEvent["stage"] {
  const normalized = message.toLowerCase();
  if (/username|password|account setup|default unix user/.test(normalized)) {
    return "awaiting_wsl_user_setup";
  }
  if (/homebrew|\bbrew\b/.test(normalized)) {
    return "installing_homebrew";
  }
  if (
    /node|npm|runtime|apt-get|dependency|dependencies|package/.test(normalized)
  ) {
    return "installing_runtime";
  }
  if (/wsl|ubuntu|distro/.test(normalized)) {
    return "installing_wsl";
  }
  return "installing_runtime";
}

