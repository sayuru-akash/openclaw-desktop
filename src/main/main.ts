function extractProtocolUrl(argv: string[]): string | null {
  const match = argv.find((value) =>
    /^openclawdesktop:\/\//i.test(value.trim()),
  );
  return match ? match.trim() : null;
}

function handleProtocolOpen(url: string): void {
  pendingProtocolUrl = url;
  if (app.isReady()) {
    showMainWindow();
  }
}

function normalizeAuthBaseUrl(url: string): string {
  return url.replace(/\/+$/, "");
}

function resolveAuthBaseUrl(config: AppConfig): string {
  const envValue = process.env.OPENCLAW_AUTH_WEB_BASE_URL?.trim();
  if (envValue) {
    return normalizeAuthBaseUrl(envValue);
  }

  const configValue = config.authWebBaseUrl?.trim();
  if (configValue) {
    return normalizeAuthBaseUrl(configValue);
  }

  return "https://auth.openclawdesk.top";
}

async function getAuthSessionStatus(): Promise<AuthSessionStatus> {
  const config = await configStore.load();
  const baseUrl = resolveAuthBaseUrl(config);
  return {
    baseUrl,
    reachable: true,
    authenticated: Boolean(config.accountAuthorized),
    userId: config.accountUserId?.trim() ? config.accountUserId : null,
  };
}

function createDesktopCallbackCapture(
  expectedState: string,
  timeoutMs = 5 * 60 * 1000,
): Promise<{
  callbackUrl: string;
  tokenPromise: Promise<string>;
}> {
  return new Promise((resolve, reject) => {
    let settled = false;
    let resolved = false;
    let timeout: NodeJS.Timeout | null = null;
    let codeResolve: (value: string | PromiseLike<string>) => void = () => {};
    let codeReject: (reason?: unknown) => void = () => {};

    const server = createServer((request, response) => {
      const sendHtml = (statusCode: number, title: string, message: string) => {
        response.statusCode = statusCode;
        response.setHeader("content-type", "text/html; charset=utf-8");
        response.end(
          `<!doctype html><html><body style="font-family:Segoe UI,Arial,sans-serif;padding:24px"><h2>${title}</h2><p>${message}</p></body></html>`,
        );
      };

      if (!request.url) {
        sendHtml(400, "Invalid request", "Missing callback URL.");
        return;
      }

      const url = new URL(request.url, "http://127.0.0.1");
      if (url.pathname !== "/callback") {
        sendHtml(
          404,
          "Not found",
          "Use the desktop callback URL generated by OpenClaw Desktop.",
        );
        return;
      }

      const state = url.searchParams.get("state") ?? "";
      const token = url.searchParams.get("token") ?? "";
      const errorText = url.searchParams.get("error") ?? "";

      if (errorText) {
        sendHtml(
          400,
          "Sign-in failed",
          `Auth service returned an error: ${errorText}`,
        );
        return;
      }

      if (!state || state !== expectedState) {
        sendHtml(
          400,
          "Invalid callback",
          "State mismatch. Restart sign-in from the desktop app.",
        );
        return;
      }

      if (!token) {
        sendHtml(
          400,
          "Invalid callback",
          "Missing token. Restart sign-in from the desktop app.",
        );
        return;
      }

      sendHtml(
        200,
        "Connected",
        "OpenClaw Desktop is now linked. You can close this tab.",
      );
      settle(null, token);
    });

    const closeServer = () =>
      new Promise<void>((closeResolve) => {
        server.close(() => closeResolve());
      });

    const settle = (error: Error | null, code?: string) => {
      if (settled) {
        return;
      }
      settled = true;
      if (timeout) {
        clearTimeout(timeout);
      }
      void closeServer().finally(() => {
        if (error) {
          codeReject(error);
          return;
        }
        codeResolve(code ?? "");
      });
    };

    const codePromise = new Promise<string>((resolveCode, rejectCode) => {
      // Keep resolvers outside this scope for callback and timeout paths.
      codeResolve = resolveCode;
      codeReject = rejectCode;

      server.on("error", (error) => {
        const message = error instanceof Error ? error.message : String(error);
        const wrapped = new Error(`Auth callback server failed: ${message}`);
        if (!resolved) {
          reject(wrapped);
          resolved = true;
        }
        settle(wrapped);
      });

      timeout = setTimeout(() => {
        settle(new Error("Sign-in timed out. Please try again."));
      }, timeoutMs);

      server.listen(0, "127.0.0.1", () => {
        if (resolved) {
          return;
        }
        const address = server.address();
        if (!address || typeof address === "string") {
          const error = new Error("Could not bind local auth callback port.");
          resolved = true;
          reject(error);
          settle(error);
          return;
        }

        resolved = true;
        resolve({
          callbackUrl: `http://127.0.0.1:${address.port}/callback`,
          tokenPromise: codePromise,
        });
      });
    });
  });
}

async function runAuthHandoff(): Promise<AuthSessionStatus> {
  if (activeAuthHandoff) {
    return activeAuthHandoff;
  }

  activeAuthHandoff = (async () => {
    const config = await configStore.load();
    const baseUrl = resolveAuthBaseUrl(config);
    const state = randomBytes(24).toString("hex");

    try {
      const capture = await createDesktopCallbackCapture(state);
      const startUrl = new URL("/desktop/start", baseUrl);
      startUrl.searchParams.set("state", state);
      startUrl.searchParams.set("callback", capture.callbackUrl);

      await shell.openExternal(startUrl.toString());
      const token = await capture.tokenPromise;

      const exchangeUrl = new URL("/api/auth/desktop/exchange", baseUrl);
      const response = await fetch(exchangeUrl.toString(), {
        method: "POST",
        headers: {
          "content-type": "application/json",
          accept: "application/json",
        },
        body: JSON.stringify({ token, state }),
      });

      const payload = (await response.json().catch(() => ({}))) as {
        authenticated?: unknown;
        userId?: unknown;
        error?: unknown;
      };

      if (!response.ok) {
        const detail =
          typeof payload.error === "string"
            ? payload.error
            : `Exchange failed (${response.status}).`;
        return {
          baseUrl,
          reachable: true,
          authenticated: false,
          userId: null,
          error: detail,
        };
      }

      const authenticated = payload.authenticated === true;
      const userId = typeof payload.userId === "string" ? payload.userId : null;

      await configStore.save({
        authWebBaseUrl: baseUrl,
        accountAuthorized: authenticated,
        accountUserId: authenticated ? (userId ?? "") : "",
      });

      return {
        baseUrl,
        reachable: true,
        authenticated,
        userId,
      };
    } catch (error) {
      return {
        baseUrl,
        reachable: false,
        authenticated: false,
        userId: null,
        error: error instanceof Error ? error.message : String(error),
      };
    }
  })().finally(() => {
    activeAuthHandoff = null;
  });

  return activeAuthHandoff;
}

function resolvePreloadFile(): string {
  return path.join(__dirname, "..", "preload", "preload.js");
}

function resolveRendererFile(): string {
  const appPath = app.getAppPath();
  const reactRendererPath = path.join(
    appPath,
    "dist",
    "renderer-react",
    "index.html",
  );
  const legacyRendererPath = path.join(
    appPath,
    "dist",
    "renderer",
    "index.html",
  );
  const uiMode = (process.env.OPENCLAW_UI_EXPERIMENT || "react").toLowerCase();

  // React renderer is now the default. Set OPENCLAW_UI_EXPERIMENT=legacy to force the old UI.
  if (uiMode !== "legacy" && existsSync(reactRendererPath)) {
    return reactRendererPath;
  }

  return legacyRendererPath;
}

